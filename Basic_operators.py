#Basic operators ie Maths
# "+"" symbol is addition
print(1+1)

# "-"symbol is subtraction
print()
print(2-1)


# "*" symbol is multiplication
print()
print(2*2)

# "/" symbol is division (note: single \ generates float ie fraction)
print()
print(7/2)

# "//" symbol is division (note: double \ generates integer and typically rounds down)
print()
print(7//2)

# "%" symbol is division which returns remainder (note: value returned is only value of remaining regardless of number of divisions)
print()
print(7%5)

# "**" symbol is exponent ie 2^4 (note: if one of the arguments is a float then result = float)
print()
print(2**4)