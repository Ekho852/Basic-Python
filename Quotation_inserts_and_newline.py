#Basic operators ie Maths
# "+"" symbol is addition
print(1+1)
# "-"symbol is subtraction
print(2-1)
# "*" symbol is multiplication
print(2*2)
# "/" symbol is division (note:)
print(5/2)
# "//" symbol is division (note:)
print(5//2)
# "%" symbol is division (note:)
print(1%2)
# "**" symbol is exponent ie 2^4
print(2**4)
print(2^4)